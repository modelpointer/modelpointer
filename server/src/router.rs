    // ── SseLineBuffer (cross-chunk reassembly) ───────────────────────────────

    #[test]
    fn sse_line_buffer_reassembles_split_usage_line() {
        // The usage `data:` line is split across two network chunks, with the
        // split landing in the middle of the JSON. A naive per-chunk parser
        // would miss it entirely.
        let full = b"data: {\"usage\":{\"prompt_tokens\":10,\"completion_tokens\":5,\"total_tokens\":15}}\n";
        let (a, b) = full.split_at(20);

        let mut buf = SseLineBuffer::new();
        let mut last = TokenUsage::default();

        buf.push(a, |line| {
            if let Some(u) = extract_usage_from_sse_chunk(line) {
                last = u;
            }
        });
        // Nothing complete yet → no usage parsed from the first half.
        assert_eq!(last.total_tokens, 0);

        buf.push(b, |line| {
            if let Some(u) = extract_usage_from_sse_chunk(line) {
                last = u;
            }
        });
        assert_eq!((last.prompt_tokens, last.completion_tokens, last.total_tokens), (10, 5, 15));
    }

    #[test]
    fn sse_line_buffer_flushes_unterminated_trailing_line() {
        // Final usage line arrives without a trailing newline; flush() must still parse it.
        let mut buf = SseLineBuffer::new();
        let mut last = TokenUsage::default();
        buf.push(
            b"data: {\"usage\":{\"prompt_tokens\":3,\"completion_tokens\":7,\"total_tokens\":10}}",
            |line| {
                if let Some(u) = extract_usage_from_sse_chunk(line) {
                    last = u;
                }
            },
        );
        assert_eq!(last.total_tokens, 0, "no newline yet → buffered, not parsed");
        buf.flush(|line| {
            if let Some(u) = extract_usage_from_sse_chunk(line) {
                last = u;
            }
        });
        assert_eq!((last.prompt_tokens, last.completion_tokens, last.total_tokens), (3, 7, 10));
    }

    #[test]
    fn sse_line_buffer_handles_utf8_split_across_chunks() {
        // A multi-byte UTF-8 sequence (— = E2 80 94) is split across the chunk
        // boundary. Byte-level buffering must not corrupt or drop the line.
        let full = "data: {\"content\":\"—\",\"usage\":{\"prompt_tokens\":1,\"completion_tokens\":2,\"total_tokens\":3}}\n".as_bytes().to_vec();
        // Split inside the em-dash bytes.
        let dash_pos = full.windows(3).position(|w| w == [0xE2, 0x80, 0x94]).unwrap();
        let (a, b) = full.split_at(dash_pos + 1);

        let mut buf = SseLineBuffer::new();
        let mut last = TokenUsage::default();
        let mut parse = |line: &[u8]| {
            if let Some(u) = extract_usage_from_sse_chunk(line) {
                last = u;
            }
        };
        buf.push(a, &mut parse);
        buf.push(b, &mut parse);
        assert_eq!(last.total_tokens, 3);
    }


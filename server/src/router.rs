    async fn route_chat(
        &self,
        body: &ChatCompletionRequest,
        model_id: Option<&str>,
        headers: &HeaderMap,
        ctx: &RequestContext,
    ) -> Response {
        info!("Routing chat completion request for model: {}", body.model);
        let start = Instant::now();
        let model = model_id.unwrap_or(body.model.as_str());
        let streaming = body.stream;

        let payload = match to_value(body) {
            Ok(v) => v,
            Err(e) => {
                Metrics::record_gateway_error(model, ENDPOINT_CHAT, metrics_labels::ERROR_VALIDATION);
                return error_responses::bad_request(format!("Failed to serialize request: {}", e));
            }
        };

        let routing_key_owned = extract_routing_key(headers);
        let routing_key = routing_key_owned.as_deref();

        Metrics::record_gateway_request(model, ENDPOINT_CHAT, bool_to_static_str(streaming));

        let log_request_body = self.log_request_body;
        let inject_stream_usage = self.inject_stream_usage;
        let log_sink = self.log_sink.clone();
        let req_id = ctx.request_id.clone();
        let api_key_id = ctx.api_key_id.clone();
        // Kept unrefined — each retry attempt re-selects an upstream and refines
        // record_primary_tpm based on the selected upstream's actual priority tier.
        let rate_limit_base = ctx.rate_limit.clone();
        // Base payload without model rewrite; the rewrite is applied per attempt
        // after upstream selection (different upstreams may have different model names).
        let payload_json = Arc::new(payload);
        let client = self.client.clone();
        // Upstream selection is deferred to each retry attempt so the router
        // re-evaluates circuit-breaker state and SWRR weights on every try,
        // enabling automatic fallback-tier switch when the primary is unhealthy.
        let registry = Arc::clone(&self.upstream_registry);
        let min_priority = ctx.min_priority;
        let is_streaming = body.stream;

        let last_provider = Arc::new(Mutex::new(String::new()));
        let last_upstream_model = Arc::new(Mutex::new(String::new()));
        let response = RetryExecutor::execute_response_with_retry(
            &self.retry_config,
            |attempt| {
                // Re-select on every attempt: respects updated circuit-breaker state
                // and SWRR weights, enabling fallback-tier switch if primary is unhealthy.
                let upstream_result = registry.select_with_min_priority(
                    model, routing_key, Some(&RuntimeType::External),
                    Some(&ApiCompatibility::OpenAi), min_priority,
                );

                let (attempt_url, attempt_payload, attempt_api_key, attempt_rate_limit) =
                    match &upstream_result {
                        Some(u) => {
                            let url = format!("{}/chat/completions", u.base_url());
                            let mut p = (*payload_json).clone();
                            if let Some(name) = u.upstream_model_name() {
                                p["model"] = serde_json::Value::String(name.to_string());
                            }
                            if is_streaming && inject_stream_usage && p.get("stream_options").is_none() {
                                p["stream_options"] = serde_json::json!({"include_usage": true});
                            }
                            let key = u.api_key().clone();
                            let mut rl = rate_limit_base.clone();
                            if let Some(ref mut r) = rl {
                                r.record_primary_tpm &= u.priority() == 0;
                            }
                            (url, p, key, rl)
                        }
                        None => (String::new(), serde_json::Value::Null, None, None),
                    };

                let client = client.clone();
                let req_id = req_id.clone();
                let api_key_id = api_key_id.clone();

                let upstream_span = info_span!(
                    target: "modelpointer::otel-trace",
                    "upstream_call",
                    upstream_url = %attempt_url,
                    attempt,
                    status_code = Empty,
                    latency_ms = Empty,
                    ttft_ms = Empty,
                    error = Empty,
                );
                let log_sink = log_sink.clone();
                let lp = Arc::clone(&last_provider);
                let lum = Arc::clone(&last_upstream_model);

                async move {
                    let upstream = match upstream_result {
                        Some(u) => u,
                        None => {
                            Metrics::record_gateway_error(model, ENDPOINT_CHAT, metrics_labels::ERROR_NO_UPSTREAM);
                            return error_responses::model_not_found(model);
                        }
                    };
                    *lp.lock().unwrap() = upstream.base_url().to_string();
                    *lum.lock().unwrap() = upstream.upstream_model_name().unwrap_or(model).to_string();
                    let req_body_str = if log_request_body {
                        serde_json::to_string(&attempt_payload).unwrap_or_default()
                    } else {
                        String::new()
                    };
                    let mut req = client.post(&attempt_url).json(&attempt_payload);
                    let auth_header = attempt_api_key.as_deref()
                        .and_then(|k| HeaderValue::from_str(&format!("Bearer {}", k)).ok());
                    req = apply_provider_headers(req, upstream.api_compatibility(), auth_header.as_ref());

                    if is_streaming {
                        req = req.header("Accept", "text/event-stream");
                    }

                    let send_start = Instant::now();
                    let resp = match req.send().await {
                        Ok(r) => r,
                        Err(e) => {
                            tracing::Span::current().record("error", e.to_string().as_str());
                            Metrics::record_upstream_request(model, upstream.base_url(), 0);
                            upstream.circuit_breaker().record_failure();
                            return error_responses::service_unavailable(format!("Failed to contact upstream: {}", e));
                        }
                    };

                    let status = StatusCode::from_u16(resp.status().as_u16())
                        .unwrap_or(StatusCode::INTERNAL_SERVER_ERROR);
                    tracing::Span::current().record("status_code", status.as_u16());
                    Metrics::record_upstream_request(model, upstream.base_url(), status.as_u16());

                    // Enter the non-streaming path when:
                    //   1. The client requested a non-streaming response, OR
                    //   2. The upstream returned a non-2xx status on a streaming request.
                    // Case 2 lets RetryExecutor see the real status code and decide whether
                    // to retry, instead of spawning a dead SSE reader and issuing another
                    // upstream POST on every retry attempt.
                    if !is_streaming || !status.is_success() {
                        let content_type = resp.headers().get(CONTENT_TYPE).cloned();
                        match resp.bytes().await {
                            Ok(body_bytes) => {
                                let elapsed = send_start.elapsed();
                                tracing::Span::current().record("latency_ms", elapsed.as_millis() as u64);
                                Metrics::record_upstream_duration(model, upstream.base_url(), elapsed);
                                if status.is_success() {
                                    upstream.circuit_breaker().record_success();
                                    let usage = extract_usage_from_json(&body_bytes).unwrap_or_default();
                                    if usage.total_tokens > 0 {
                                        if let Some(ref rl) = attempt_rate_limit {
                                            rl.record_tokens(&api_key_id, usage.total_tokens).await;
                                        }
                                    }
                                    let finish_reason = extract_finish_reason_from_json(&body_bytes);
                                    let resp_body_str = if log_request_body {
                                        String::from_utf8_lossy(&body_bytes).into_owned()
                                    } else {
                                        String::new()
                                    };
                                    emit_access_log(
                                        &log_sink,
                                        &req_id, &api_key_id, model, ENDPOINT_CHAT,
                                        upstream.base_url(), upstream.upstream_model_name().unwrap_or(model), status.as_u16(),
                                        elapsed.as_millis() as u64,
                                        false, 0, 0.0,
                                        usage.prompt_tokens, usage.completion_tokens, usage.total_tokens,
                                        &finish_reason, &req_body_str, &resp_body_str,
                                    );
                                } else {
                                    upstream.circuit_breaker().record_failure();
                                }
                                let mut response = Response::new(Body::from(body_bytes));
                                *response.status_mut() = status;
                                if let Some(ct) = content_type {
                                    response.headers_mut().insert(CONTENT_TYPE, ct);
                                }
                                response
                            }
                            Err(e) => {
                                tracing::Span::current().record("error", e.to_string().as_str());
                                upstream.circuit_breaker().record_failure();
                                error_responses::internal_error(format!("Failed to read response: {}", e))
                            }
                        }
                    } else {
                        let ttfb = send_start.elapsed();
                        tracing::Span::current().record("latency_ms", ttfb.as_millis() as u64);
                        Metrics::record_upstream_duration(model, upstream.base_url(), ttfb);
                        // Circuit breaker recording is deferred to the stream task so that
                        // mid-stream upstream failures are captured.
                        let is_success_status = status.is_success();
                        let status_code_u16 = status.as_u16();
                        let stream = resp.bytes_stream();
                        let (tx, rx) = mpsc::channel(64);
                        let current_span = tracing::Span::current();
                        let model_id = model.to_string();
                        let provider = upstream.base_url().to_string();
                        let upstream_model_str = upstream.upstream_model_name().unwrap_or(model).to_string();
                        let rate_limit_spawn = attempt_rate_limit.clone();
                        let cb_upstream = Arc::clone(&upstream);
                        tokio::spawn(async move {
                            let mut ttft_ms = 0u64;
                            let mut last_usage = TokenUsage::default();
                            let mut last_finish_reason = String::new();
                            let mut resp_buf = if log_request_body { Some(Vec::new()) } else { None };
                            let mut upstream_error = false;
                            let mut first = true;
                            let mut s = stream;
                            let mut line_buf = SseLineBuffer::new();
                            while let Some(chunk) = s.next().await {
                                if first {
                                    let ttft = send_start.elapsed();
                                    ttft_ms = ttft.as_millis() as u64;
                                    current_span.record("ttft_ms", ttft_ms);
                                    Metrics::record_upstream_ttft(&model_id, &provider, ttft);
                                    first = false;
                                }
                                match chunk {
                                    Ok(bytes) => {
                                        // Parse only complete lines; partial lines split across
                                        // chunks are buffered until their newline arrives.
                                        line_buf.push(&bytes, |line| {
                                            if let Some(u) = extract_usage_from_sse_chunk(line) {
                                                last_usage = u;
                                            }
                                            if let Some(r) = extract_finish_reason_from_sse_chunk(line) {
                                                last_finish_reason = r;
                                            }
                                        });
                                        if let Some(buf) = resp_buf.as_mut() {
                                            if buf.len() < MAX_LOG_BODY_BYTES {
                                                buf.extend_from_slice(&bytes);
                                            }
                                        }
                                        if tx.send(Ok(bytes)).await.is_err() {
                                            break;
                                        }
                                    }
                                    Err(e) => {
                                        upstream_error = true;
                                        let _ = tx.send(Err(format!("Stream error: {}", e))).await;
                                        break;
                                    }
                                }
                            }
                            // Parse any trailing partial line left after the stream ends.
                            line_buf.flush(|line| {
                                if let Some(u) = extract_usage_from_sse_chunk(line) {
                                    last_usage = u;
                                }
                                if let Some(r) = extract_finish_reason_from_sse_chunk(line) {
                                    last_finish_reason = r;
                                }
                            });
                            // Deferred circuit breaker: a success-status response is only
                            // truly successful when the stream drains without error.
                            // Client disconnect (tx.send error) is not the upstream's fault.
                            if is_success_status {
                                if upstream_error {
                                    cb_upstream.circuit_breaker().record_failure();
                                } else {
                                    cb_upstream.circuit_breaker().record_success();
                                }
                            }
                            // Stream completed — record tokens then emit access log
                            if last_usage.total_tokens > 0 {
                                if let Some(ref rl) = rate_limit_spawn {
                                    rl.record_tokens(&api_key_id, last_usage.total_tokens).await;
                                }
                            }
                            let total_ms = send_start.elapsed().as_millis() as u64;
                            let tpot_ms = if last_usage.completion_tokens > 0 && total_ms > ttft_ms {
                                (total_ms - ttft_ms) as f64 / last_usage.completion_tokens as f64
                            } else {
                                0.0
                            };
                            if last_usage.completion_tokens > 0 {
                                Metrics::record_tpot(&model_id, &provider, tpot_ms);
                            }
                            let resp_body_str = resp_buf
                                .map(|b| String::from_utf8_lossy(&b).into_owned())
                                .unwrap_or_default();
                            emit_access_log(
                                &log_sink,
                                &req_id, &api_key_id, &model_id, ENDPOINT_CHAT,
                                &provider, &upstream_model_str, status_code_u16,
                                total_ms,
                                true, ttft_ms, tpot_ms,
                                last_usage.prompt_tokens, last_usage.completion_tokens, last_usage.total_tokens,
                                &last_finish_reason, &req_body_str, &resp_body_str,
                            );
                        });
                        let mut response =
                            Response::new(Body::from_stream(ReceiverStream::new(rx)));
                        *response.status_mut() = status;
                        response
                            .headers_mut()
                            .insert(CONTENT_TYPE, HeaderValue::from_static("text/event-stream"));
                        response
                    }
                }.instrument(upstream_span)
            },
            |res, _attempt| is_retryable_status(res.status()),
            |_delay, _next_attempt, status_code| {
                Metrics::record_retry_attempt(model, ENDPOINT_CHAT, status_code);
            },
            || {
                Metrics::record_retry_exhausted(model, ENDPOINT_CHAT);
            },
        )
        .await;

        if response.status().is_success() {
            Metrics::record_gateway_duration(model, ENDPOINT_CHAT, start.elapsed());
        } else {
            let provider = last_provider.lock().unwrap().clone();
            let upstream_model = last_upstream_model.lock().unwrap().clone();
            emit_access_log(
                &log_sink,
                &req_id, &api_key_id, model, ENDPOINT_CHAT,
                &provider, &upstream_model, response.status().as_u16(),
                start.elapsed().as_millis() as u64,
                is_streaming, 0, 0.0, 0, 0, 0, "", "", "",
            );
            Metrics::record_gateway_error(model, ENDPOINT_CHAT, metrics_labels::ERROR_BACKEND);
        }

        response
    }


/// Scan an OpenAI SSE chunk for a non-null `choices[0].finish_reason`.
fn extract_finish_reason_from_sse_chunk(bytes: &[u8]) -> Option<String> {
    let text = std::str::from_utf8(bytes).ok()?;
    for line in text.lines() {
        if let Some(json_str) = line.strip_prefix("data:").map(str::trim_start) {
            if json_str.trim() == "[DONE]" { continue; }
            if let Ok(v) = serde_json::from_str::<serde_json::Value>(json_str) {
                if let Some(reason) = v.get("choices")
                    .and_then(|c| c.get(0))
                    .and_then(|c| c.get("finish_reason"))
                    .and_then(|r| r.as_str())
                    .filter(|s| !s.is_empty())
                {
                    return Some(reason.to_string());
                }
            }
        }
    }
    None
}

/// Scan an Anthropic SSE chunk for `stop_reason` in a `message_delta` event.
fn extract_anthropic_finish_reason_from_sse_chunk(bytes: &[u8]) -> Option<String> {
    let text = std::str::from_utf8(bytes).ok()?;
    for line in text.lines() {
        if let Some(json_str) = line.strip_prefix("data:").map(str::trim_start) {
            if let Ok(v) = serde_json::from_str::<serde_json::Value>(json_str) {
                if v.get("type").and_then(|t| t.as_str()) == Some("message_delta") {
                    if let Some(reason) = v.get("delta")
                        .and_then(|d| d.get("stop_reason"))
                        .and_then(|r| r.as_str())
                        .filter(|s| !s.is_empty())
                    {
                        return Some(reason.to_string());
                    }
                }
            }
        }
    }
    None
}

/// Re-assembles SSE `data:` lines that are split across network chunks.
///
/// `reqwest`'s `bytes_stream()` yields arbitrary transfer-layer chunks: a single
/// `data: {...}` line (e.g. the trailing `usage` block) may be split across two
/// chunks, or a chunk may end in the middle of a multi-byte UTF-8 sequence. The
/// per-chunk parsers (`extract_usage_from_sse_chunk`, etc.) would then silently
/// drop that line, under-counting TPM and missing `finish_reason` / TPOT.
///
/// This buffer feeds the parsers only *complete* lines (everything up to the
/// last newline seen so far) and retains the incomplete trailing fragment for
/// the next chunk. Memory is bounded to a single incomplete line, not the whole
/// response body.
struct SseLineBuffer {
    carry: Vec<u8>,
}

impl SseLineBuffer {
    /// Drop the carry if a single line grows past this without a newline. Guards
    /// against unbounded growth from a misbehaving upstream that never emits one.
    const MAX_CARRY: usize = 1 << 20; // 1 MiB

    fn new() -> Self {
        Self { carry: Vec::new() }
    }

    /// Append `bytes`, then invoke `parse` once on the span of newly-completed
    /// lines (up to and including the last newline). Each line is parsed exactly
    /// once — in the call where it completes. The incomplete tail is kept.
    fn push(&mut self, bytes: &[u8], mut parse: impl FnMut(&[u8])) {
        self.carry.extend_from_slice(bytes);
        if let Some(nl) = self.carry.iter().rposition(|&b| b == b'\n') {
            parse(&self.carry[..=nl]);
            self.carry.drain(..=nl);
        }
        if self.carry.len() > Self::MAX_CARRY {
            self.carry.clear();
        }
    }

    /// Parse any bytes left after the stream ends (a final line that was not
    /// newline-terminated).
    fn flush(&mut self, mut parse: impl FnMut(&[u8])) {
        if !self.carry.is_empty() {
            parse(&self.carry);
            self.carry.clear();
        }
    }
}

